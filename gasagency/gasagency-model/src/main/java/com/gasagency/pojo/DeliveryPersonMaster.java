package com.gasagency.pojo;

public class DeliveryPersonMaster 
{
	private int deliveryPersonId;
	private String firstName;
	private String lastName;
	private String phoneNumber;
	private int deliveryPersonType;
	public int getDeliveryPersonId() {
		return deliveryPersonId;
	}
	public void setDeliveryPersonId(int deliveryPersonId) {
		this.deliveryPersonId = deliveryPersonId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public int getDeliveryPersonType() {
		return deliveryPersonType;
	}
	public void setDeliveryPersonType(int deliveryPersonType) {
		this.deliveryPersonType = deliveryPersonType;
	}
	
	
}
